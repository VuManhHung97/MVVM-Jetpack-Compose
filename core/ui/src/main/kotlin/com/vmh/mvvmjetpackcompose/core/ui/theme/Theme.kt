@Composable
fun MVVMJetpackComposeTheme(useDarkTheme: Boolean = true, content: @Composable () -> Unit) {
  val colors = if (useDarkTheme) {
    darkScheme
  } else {
    lightScheme
  }

  MaterialTheme(
    colorScheme = colors,
    shapes = Shapes,
    content = {
      CompositionLocalProvider(content = content)
    },
  )
}

private val LocalTypography: ProvidableCompositionLocal<Typography> =
  staticCompositionLocalOf { error("No Typography provided") }

object MVVMJetpackComposeTheme {
  val typography: Typography
    @Composable
    @ReadOnlyComposable
    get() = LocalTypography.current
}
